# все 1 перешли в нули, все 2 перешли в 1, все 0 перешли в 2
# если в начальной строке было х символов 2, то 2х было символов 0, чтобы соблюсти конченое условие
# решаем уравнение 2 * х + 2 * х = 448, откуда х = 112. Выкидываем из 1000 кол-во символов 0 и 2 и получаем кол-во единиц:
print(1000 - 3 * 112)

